package tst.project.bean.hx;

public class HXBean {
	private String error;
	private String access_token;
	private String expires_in;
	private String application;
	public String getError() {
		return error;
	}
	public HXBean setError(String error) {
		this.error = error;
		return this;
	}
	public String getAccess_token() {
		return access_token;
	}
	public HXBean setAccess_token(String access_token) {
		this.access_token = access_token;
		return this;
	}
	public String getExpires_in() {
		return expires_in;
	}
	public HXBean setExpires_in(String expires_in) {
		this.expires_in = expires_in;
		return this;
	}
	public String getApplication() {
		return application;
	}
	public HXBean setApplication(String application) {
		this.application = application;
		return this;
	}
}
