package tst.project.bean.others;

public class MsgBean {
	private String member_id;
	private String msg_title;
	private String msg_desc;
	private String create_time;
	private String msg_type;//group团购消息 pre:预售消息
	private String relation_id;//type 为group 则是团购id  为pre 则为订单id
	
	public String getMember_id() {
		return member_id;
	}
	public MsgBean setMember_id(String member_id) {
		this.member_id = member_id;
		return this;
	}
	public String getMsg_title() {
		return msg_title;
	}
	public MsgBean setMsg_title(String msg_title) {
		this.msg_title = msg_title;
		return this;
	}
	public String getMsg_desc() {
		return msg_desc;
	}
	public MsgBean setMsg_desc(String msg_desc) {
		this.msg_desc = msg_desc;
		return this;
	}
	public String getCreate_time() {
		return create_time;
	}
	public MsgBean setCreate_time(String create_time) {
		this.create_time = create_time;
		return this;
	}
	public String getMsg_type() {
		return msg_type;
	}
	public MsgBean setMsg_type(String msg_type) {
		this.msg_type = msg_type;
		return this;
	}
	public String getRelation_id() {
		return relation_id;
	}
	public MsgBean setRelation_id(String relation_id) {
		this.relation_id = relation_id;
		return this;
	}
	
}
