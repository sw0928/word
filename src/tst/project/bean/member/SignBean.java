package tst.project.bean.member;

public class SignBean {
	private int sign_id;
	private String member_id;
	private String create_time;
	private int sign_integral;//签到赠送积分
	private int sign_continuity_count_month;//一个月内 连续签到次数
	private int sign_total_count_month;//一个月内 总共签到次数

	
	public int getSign_continuity_count_month() {
		return sign_continuity_count_month;
	}
	public SignBean setSign_continuity_count_month(int sign_continuity_count_month) {
		this.sign_continuity_count_month = sign_continuity_count_month;
		return this;
	}
	public int getSign_total_count_month() {
		return sign_total_count_month;
	}
	public SignBean setSign_total_count_month(int sign_total_count_month) {
		this.sign_total_count_month = sign_total_count_month;
		return this;
	}
	
	public int getSign_integral() {
		return sign_integral;
	}
	public SignBean setSign_integral(int sign_integral) {
		this.sign_integral = sign_integral;
		return this;
	}
	public int getSign_id() {
		return sign_id;
	}
	public SignBean setSign_id(int sign_id) {
		this.sign_id = sign_id;
		return this;
	}
	public String getMember_id() {
		return member_id;
	}
	public SignBean setMember_id(String member_id) {
		this.member_id = member_id;
		return this;
	}
	public String getCreate_time() {
		return create_time;
	}
	public SignBean setCreate_time(String create_time) {
		this.create_time = create_time;
		return this;
	}
}
