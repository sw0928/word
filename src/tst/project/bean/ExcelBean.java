package tst.project.bean;

public class ExcelBean {
	private String name;
	private String type;
	public String getName() {
		return name;
	}
	public ExcelBean setName(String name) {
		this.name = name;
		return this;
	}
	public String getType() {
		return type;
	}
	public ExcelBean setType(String type) {
		this.type = type;
		return this;
	}
	
	
}
