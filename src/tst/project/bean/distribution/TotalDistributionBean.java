package tst.project.bean.distribution;

import java.util.List;

public class TotalDistributionBean {
	private float total_price;
	private int total_count;
	private int total_card_count;
	private List<DistributionBean> distributionBeans;
	
	public int getTotal_card_count() {
		return total_card_count;
	}
	
	public TotalDistributionBean setTotal_card_count(int total_card_count) {
		this.total_card_count = total_card_count;
		return this;
	}
	public int getTotal_count() {
		return total_count;
	}
	public TotalDistributionBean setTotal_count(int total_count) {
		this.total_count = total_count;
		return this;
	}
	public float getTotal_price() {
		return total_price;
	}
	public TotalDistributionBean setTotal_price(float total_price) {
		this.total_price = total_price;
		return this;
	}
	public List<DistributionBean> getDistributionBeans() {
		return distributionBeans;
	}
	public TotalDistributionBean setDistributionBeans(List<DistributionBean> distributionBeans) {
		this.distributionBeans = distributionBeans;
		return this;
	}	
}
